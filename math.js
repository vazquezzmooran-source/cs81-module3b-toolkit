// 1. Double Function - Returns double the value of a number
function double(n) {
  return n * 2;
}

// 2. Square Function - Returns the square of a number
function square(n) {
  return n * n;
}

// 3. IsEven Function - Checks if a number is even
function isEven(n) {
  return n % 2 === 0;
}

// 4. IsOdd Function - Checks if a number is odd  
function isOdd(n) {
  return n % 2 !== 0;
}

// 5. Multiply Function - Multiplies two numbers
function multiply(a, b) {
  return a * b;
}
